package xdevs.lib.dynamic;

/**
 * Class to keep the sizes of a dynamic system
 * 
 * @author J.M. de la Cruz, May 14th, 2008
 * @version 1.0
 * 
 */
public class Sizes {
    public int Nxc = 0;  // nº continuos state variable. Default = 0
    public int Nxd = 0;  // nº discrete state variables. Default = 0
    public int Nu = 1;   // dimension input vector. Default = 1
    public int Ny = 1;   // dimension ouput vector. Default = 1;
}
